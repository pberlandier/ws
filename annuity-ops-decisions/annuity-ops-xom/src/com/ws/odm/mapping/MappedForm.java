package com.ws.odm.mapping;

public class MappedForm {
	private String indexFormId;
	private String consolidatedFormName;
	private String formName;
	private String paperFormId;
	
	public String getIndexFormId() {
		return indexFormId;
	}
	public void setIndexFormId(String indexFormId) {
		this.indexFormId = indexFormId;
	}
	public String getConsolidatedFormName() {
		return consolidatedFormName;
	}
	public void setConsolidatedFormName(String consolidatedFormName) {
		this.consolidatedFormName = consolidatedFormName;
	}
	public String getFormName() {
		return formName;
	}
	public void setFormName(String formName) {
		this.formName = formName;
	}
	public String getPaperFormId() {
		return paperFormId;
	}
	public void setPaperFormId(String paperFormId) {
		this.paperFormId = paperFormId;
	}
	
	
}
